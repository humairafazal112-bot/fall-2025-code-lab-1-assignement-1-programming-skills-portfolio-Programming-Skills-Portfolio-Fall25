## Exercise 7: Some Counting 

 #loop1: counts up from 0 to 50 (step 1)
print("Counting up from 0 to 50:")
for i in range(0,51,1):
    print(i)

#loop2: counts down from 50 to 0 (step -1)
print("/ncounting down from 50 to 0:")
for i in range(50,-1,-1):
    print(i)

#loop3:  counts up from 30 to 50  (step 1)
print("/ncounting up from 30 to 50:")
for i in range(30,51):
    print(i)

#loop4: that counts down from 50 to 10 (step-2)
print("/ncounting down from 50 to 10(step -2):")
for i in range(50,9,-2):
    print(i) 

#loop5: that counts up from 100 to 200(step 5) 
